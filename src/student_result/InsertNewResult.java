    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String phy = jTextField2.getText();
        String chem = jTextField3.getText();
        String maths = jTextField4.getText();
        String em = jTextField5.getText();
        String dbms = jTextField6.getText();
        
        try{
           Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/kunaldb", "root", "Root123@");
           Statement st = con.createStatement();
           ResultSet rs = st.executeQuery("select * from student where rollno='"+rollno+"'");
           // if student present in db then only put data into it
           if(rs.next()){
               st.executeUpdate("insert into result(rollno, phy, chem, maths, em, dbms) values ('"+rollno+"', '"+phy+"', '"+chem+"', '"+maths+"', '"+em+"', '"+dbms+"')");
               JOptionPane.showMessageDialog(null, "Successfully added the data");
               setVisible(false);
               new InsertNewResult().setVisible(true);
           }else{
               JOptionPane.showMessageDialog(null, "This roll no doesnot exist.");
           }
           
           
        }
        catch(Exception e){
            
        }
    }//GEN-LAST:event_jButton6ActionPerformed

